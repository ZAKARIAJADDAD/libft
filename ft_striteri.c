#include "libft.h"

void	ft_striteri(char *s, void (*fo)(unsigned int, char*))
{
	int	i;

	if (s && fo)
	{
		i = 0;
		while (s[i])
		{
			fo(i, &s[i]);
			i++;
		}
		s[i] = '\0';
	}
}
